package javaBasics;

public class Area_Of_Circle {
	public static void main(String[] args) {
		int radius = 6 ;
		final double PI = 3.14;
		double area = PI * radius * radius ;
		System.out.println("area of circle is = "+" " + area);
		
		
	}
	public static double radius(double x) {
		double PI = 3.14 ;
		return PI * x * x ;
	}

}
 